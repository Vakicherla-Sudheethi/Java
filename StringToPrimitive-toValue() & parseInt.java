class HelloWorld {
    public static void main(String[] args) {
        //conversion from string to primitive
        //using valueOf
        String s = "2";
        int n = Integer.valueOf(s);
        System.out.println(n);
        //using Xxxvalue
        String a = "4";
        int m = Integer.parseInt(a);
        System.out.println(m);
        
    }
}
