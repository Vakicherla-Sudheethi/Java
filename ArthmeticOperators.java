class ArthmeticOperators
{
public static void main(String args[])
{
int a = 2;
int b= 4;
System.out.println(a+b);//2+4=6
System.out.println(a-b);//2-4=2
System.out.println(a*b);//2*4=8
System.out.println(a/b);//2/4=0.5=0
System.out.println(a%b);//2%4=2
System.out.println(++a);
System.out.println(--a);
}
}
