//String Methods
class StringMethods
{
public static void main(String args[])
{
String a = "Bannu";
System.out.println(a.length());//5
System.out.println(a.toUpperCase());//BANNU
System.out.println(a.toLowerCase());//bannu
String b = "Kanna";
System.out.println(a.concat(b));//BannuKanna
//Special Symbols
System.out.println("Bannu likes \"BTS\" Songs");
}
}